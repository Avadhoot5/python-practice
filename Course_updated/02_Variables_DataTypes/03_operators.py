# Arithmetic operators

a = 7
b = 2
add = a + b
sub = a - b
mul = a * b

print('Arithmetic operators result', add, sub, mul)

# Assignment operators

c = 9 - 5
d = 5
d += 2

print('Assignment operator result', c, d)

# Comparison operators

compRes = 5 < 4
compRes2 = 2 == 2
compRes3 = 5 >= 3
notEqual = 2 != 2

print('Comparison operators result')
print(compRes,compRes2,compRes3)
print(notEqual)

# Logical operators

orLogic = True or False
andLogic = True and False
notLogic = not(True)

print('or ', orLogic)
print('and ', andLogic)
print('not ', notLogic)
